#include "3-calc.h"
/**
* op_add - addition.
* @a: first integer
* @b second integer
*
* Return: sum.
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - subtract
* @a: first integer
* @b: second integer
*
* Return: difference.
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - multiplaication
* @a: first number
* @b: second number
*
* Return: multiplaication result.
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - division.
* @a: first integer
* @b: second integer
*
* Return: division result.
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - modulo.
* @a: first integer
* @b: second integer
*
* Return: remainder.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
